/*
 Write a program to display table of 2.
 */
package arraylistex;


public class TableOf2 {
    
    public static void main( String args[]){
    
        int a = 2;
        int x; 
        int i;
        
      for (i=1;i<=10;i++)
      {
      
          x = a * i;
          
          System.out.println(x);
          
      }  
    
    }
}
